#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (s != 0 && f != 0)
	{
		while (*s)
		{
			(*f)(i, s);
			s++;
			i++;
		}
	}
}
